package bean;

public class customerBean {
	private String custld;
	private String name;
	private String hp;
	private String addr;
	private String rdate;
	
	
	public String getCustld() {
		return custld;
	}
	public void setCustld(String custld) {
		this.custld = custld;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getHp() {
		return hp;
	}
	public void setHp(String hp) {
		this.hp = hp;
	}
	public String getAddr() {
		return addr;
	}
	public void setAddr(String addr) {
		this.addr = addr;
	}
	public String getRdate() {
		return rdate;
	}
	public void setRdate(String rdate) {
		this.rdate = rdate;
	}
	
	
}
